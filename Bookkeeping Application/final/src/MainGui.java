    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {

        String str = user.getPassword();
        char[] password = str.toCharArray();

        if (isPasswordCorrect(jPasswordField1.getPassword(), password)) {

            UserMainGui userMainGui = new UserMainGui(user);
            userMainGui.setVisible(true);

        } else {
            JOptionPane.showMessageDialog(this, "Invalid Password");
        }

    }
