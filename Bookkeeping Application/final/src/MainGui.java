    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {
        String str = admin.getPassword();
        char[] password = str.toCharArray();

        if (isPasswordCorrect(jPasswordField2.getPassword(), password)) {

            new AdminLoginGui(admin).setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Invalid Password");
        }
    }

     //check password is valid function
    private static boolean isPasswordCorrect(char[] input, char[] correctPassword) {
        boolean isCorrect = true;

        if (input.length != correctPassword.length) {
            isCorrect = false;
        } else {
            isCorrect = Arrays.equals(input, correctPassword);
        }

        // Zero out the password.
        Arrays.fill(correctPassword, '0');

        return isCorrect;
    }

